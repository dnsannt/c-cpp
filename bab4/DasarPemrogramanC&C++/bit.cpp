#include <stdio.h>

int main()
{
    printf("9 & 10 = %d\n", 9 & 10);
    printf("29 & 7 = %d\n", 29 & 7);

    printf("9 | 10 = %d\n", 9 | 10);
    printf("29 | 7 = %d\n", 29 | 7);

    printf("~185 = %d\n", ~185);

    printf("-29 & 7 = %d\n", -29 & 7);

    printf("29 << 1 = %d\n", 29 << 1);

    printf("29 >> 1 = %d\n", 29 >> 1);
    printf("-29 >> 1 = %d", -29 >> 1);

    return 0;
}